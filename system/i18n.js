function applyLanguage(langCode) {
    if (!langCode) langCode = localStorage.getItem('j2st_lang') || 'en';
    const lang = i18n_dict[langCode] || i18n_dict.en;
    localStorage.setItem('j2st_lang', langCode);

    // Update labels and flag in home if exists
    const currentFlag = document.getElementById('current-flag');
    const currentName = document.getElementById('current-lang-name');
    if (currentFlag) currentFlag.textContent = lang.flag;
    if (currentName) currentName.textContent = lang.label;

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (lang[key]) {
            // If it's an input or textarea with placeholder
            if ((el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') && key.endsWith('_ph')) {
                el.placeholder = lang[key];
            } else {
                el.innerHTML = lang[key];
            }
        }
    });

    // Re-parse twemoji
    if (window.twemoji) {
        twemoji.parse(document.body, { folder: 'svg', ext: '.svg', base: 'https://cdn.jsdelivr.net/gh/jdecked/twemoji@latest/assets/' });
    }
}

// Global detection
(function() {
    const urlParams = new URLSearchParams(window.location.search);
    const langParam = urlParams.get('lang');
    const pathParts = window.location.pathname.split('/');
    const langCodes = ["tr", "en", "de", "ru"];
    
    let targetLang = langParam || localStorage.getItem('j2st_lang');
    
    // Check if the current actual path starts with a lang code
    if (langCodes.includes(pathParts[1])) {
        targetLang = pathParts[1];
    }

    if (!targetLang || !i18n_dict[targetLang]) targetLang = 'en';
    
    window.addEventListener('DOMContentLoaded', () => {
        applyLanguage(targetLang);
        
        // Setup lang switcher if present
        const langDropdown = document.getElementById('lang-dropdown');
        if (langDropdown) {
             document.querySelectorAll('.lang-opt').forEach(opt => {
                opt.onclick = () => {
                    const l = opt.getAttribute('data-lang');
                    // Get current clean path (without lang)
                    const currentParts = window.location.pathname.substring(1).split('/');
                    let newPath = "";
                    if (langCodes.includes(currentParts[0])) {
                        newPath = "/" + l + "/" + currentParts.slice(1).join("/");
                    } else {
                        newPath = "/" + l + "/" + currentParts.join("/");
                    }
                    window.location.href = newPath.replace(/\/$/, "");
                };
            });
        }
    });
})();
